CREATE TABLE reviews(
	brewery_name VARCHAR,
	beer_name VARCHAR,
	beer_style VARCHAR,
	review_overall FLOAT,
	review_aroma FLOAT,
	review_palate FLOAT,
	review_taste FLOAT
);
CREATE TABLE contact(
	name VARCHAR,
	address VARCHAR,
	city VARCHAR,
	address VARCHAR,
	state VARCHAR,
	zip VARCHAR,
	phone VARCHAR
);
